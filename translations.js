// ─── Language Switcher Engine ──────────────────────────────────────────────
const LANG_META = {
  fr: { label: "Français", dir: "ltr", flag: "🇫🇷" },
  en: { label: "English",  dir: "ltr", flag: "🇬🇧" },
  ar: { label: "العربية",  dir: "rtl", flag: "🇸🇦" },
  zh: { label: "中文",     dir: "ltr", flag: "🇨🇳" },
  it: { label: "Italiano", dir: "ltr", flag: "🇮🇹" },
  es: { label: "Español",  dir: "ltr", flag: "🇪🇸" },
  ja: { label: "日本語",   dir: "ltr", flag: "🇯🇵" },
};

function applyLanguage(lang) {
  const t = NOVA_TRANSLATIONS[lang];
  if (!t) return;

  // Persist
  localStorage.setItem('nova_lang', lang);

  // Update HTML dir + lang attribute
  const meta = LANG_META[lang];
  document.documentElement.lang = lang;
  document.documentElement.dir = meta.dir;

  // Apply translations to all data-i18n elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) {
      el.textContent = t[key];
    }
  });

  // Apply placeholder translations
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (t[key] !== undefined) el.placeholder = t[key];
  });

  // RTL spacing fix for Tailwind: add rtl class to body
  if (meta.dir === 'rtl') {
    document.body.classList.add('rtl-mode');
  } else {
    document.body.classList.remove('rtl-mode');
  }
}

function initLangSwitcher() {
  const savedLang = localStorage.getItem('nova_lang') || 'fr';

  // Build lang switcher HTML
  const switcher = document.getElementById('lang-switcher');
  if (!switcher) return;

  const options = Object.entries(LANG_META).map(([code, meta]) =>
    `<button onclick="applyLanguage('${code}')" data-lang="${code}" title="${meta.label}"
      class="lang-btn flex items-center gap-1.5 px-3 py-1.5 rounded-lg text-xs font-bold uppercase tracking-wide
             hover:bg-white/10 transition-all whitespace-nowrap text-white/60 hover:text-white">
      <span>${meta.flag}</span><span class="hidden md:inline">${meta.label}</span>
    </button>`
  ).join('');

  switcher.innerHTML = `
    <div class="flex items-center gap-1 p-1 bg-white/5 border border-white/10 rounded-xl">
      ${options}
    </div>`;

  applyLanguage(savedLang);
}

document.addEventListener('DOMContentLoaded', initLangSwitcher);
